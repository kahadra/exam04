char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	l;

	i = 0;
	while (dest[i] != '\0')
		i++;
	l = i;
	i = 0;
	while (str[i] != '\0')
	{
		dest[l + i] = str[i];
		i++;
	}
	dest[l + i] = '\0';
	return (dest);
}
